/* VARIAVEIS */

// tipos:  var, const e let.

// const = o tipo de variavel const não pode ter o valor mudado, ou seja, constante.

// var = pode ter valor mudado, e pode não ter valor pré definido.
    // sem valor pré definido (var nome);
    // com valor pré definido (var nome = sophia);

// let = pode ter valor mudado, e também nao precisa ter valor pré definido.
    // sem valor pré definido (let nome);
    // com valor pré definido (let nome = sophia);